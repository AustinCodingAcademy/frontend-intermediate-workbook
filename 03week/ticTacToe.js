function ticTacToe(row, column) {
  //IF player enters nothing
  if (!row || !column) {
    return "You must enter a valid row/column!";
  }
  //If player enters number higher than 2
  if (parseInt(row) >= 3) {
    return "You must enter a row less than 3!";
  } else if (parseInt(column) >= 3) {
    return "You must enter a column less than 3!";
  }

  // Your code here
  //if player is X
  if (playerTurn === "X") {
    if (board[row].includes("X", column) || board[row].includes("O", column)) {
      board[row].splice(column, 1, "X");
      playerTurn = "O";
    } else {
      return "Space Occupied!";
    }
  } else {
    //Player is O
    if (
      board[row].indexOf(column) != "X" &&
      board[row].indexOf(column) != "O"
    ) {
      board[row].splice(column, 1, "O");
      playerTurn = "X";
    } else {
      return "Space Occupied!";
    }
  }
}

